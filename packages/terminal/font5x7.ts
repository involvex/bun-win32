export const PIXEL_FONT_WIDTH = 5;
export const PIXEL_FONT_HEIGHT = 7;

/** Glyph character → packed `PIXEL_FONT_WIDTH × PIXEL_FONT_HEIGHT` bitmap (1 = lit). */
export const pixelFont = new Map<string, Uint8Array>();
for (const [character, rows] of Object.entries(fontRows)) {
  const bitmap = new Uint8Array(PIXEL_FONT_WIDTH * PIXEL_FONT_HEIGHT);
  for (let row = 0; row < PIXEL_FONT_HEIGHT; row++) {
    const rowText = rows[row] ?? '';
    for (let column = 0; column < PIXEL_FONT_WIDTH; column++) {
      bitmap[row * PIXEL_FONT_WIDTH + column] = rowText[column] === '#' ? 1 : 0;
    }
  }
  pixelFont.set(character, bitmap);
}
